package librisuite.business.common;

public class EditorDigitException extends Exception
{
	private static final long serialVersionUID = -8207968816842862351L;

	public EditorDigitException() {
		super();
	}

	public EditorDigitException(String message, Throwable cause) {
		super(message, cause);
	}

	public EditorDigitException(String message) {
		super(message);
	}

	public EditorDigitException(Throwable cause) {
		super(cause);
	}
}
